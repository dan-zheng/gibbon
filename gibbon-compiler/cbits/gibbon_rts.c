/*

  ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  Memory Management; regions, chunks, GC etc.
  ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

  Gibbon has "growing regions" i.e each logical region is backed by a doubly linked-list
  of smaller chunks which grows as required. In addition to actual data, each chunk
  stores some additional metadata (GibRegionFooter) to chain the chunks together in a list
  and for garbage collection. The footer:

  ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  serialized data | rf_reg_metadata_ptr | rf_seq_no | rf_nursery_allocated | rf_size | rf_next | rf_prev
  ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

  The metadata after the serialized data serves various purposes:

  - rf_reg_metadata_ptr: A pointer to a GibRegionMeta struct that contains various metadata.
  Of particular interest to us are the fields:

  = reg_id: A unique identifier for a region.

  = refcount and outset: Whenever an inter-region indirection is created, we record that information
  using these two fields. Suppose we have an indirection from region A that points to some chunk
  in region B. Then A's outset will store a pointer to that chunk's footer, and B's refcount will
  be bumped by 1. Note that all there's only 1 refcount cell, and 1 outset per logical region,
  and chunks only store a pointer to them.

  - rf_seq_no: The index of this particular chunk in the list.

  - rf_nursery_allocated: Whether this chunk was allocated in a nursery.

  - rf_size: Used during bounds checking to calculate the size of the next region in
  the linked list.

  - rf_next / rf_prev: Point to the next and previous chunk respectively.


  There are two ways in which a region may be freed:

  (1) Whenever it goes out of scope

  The RTS tries to free a region whenever it goes out of scope. But this doesn't always succeed as
  regions sometimes contain values that "escape". One reason why this'll happen is if there's an
  indirection from A->B, and A lives longer than B.
  In such a case, when B goes out of scope it's refcount won't be 0, and the RTS won't free it.
  This brings us to (2).

  (2)

  When the RTS successfully frees a region, it decrements the refcounts of all the regions it
  points to (via the outset). At the same time, if it encounters a region in the outset whoose
  refcount becomes 0 after the decrement, it calls gib_free_region on that. This way we can be sure
  that all regions will eventually be garbage collected before the program exits.



  Why is it a doubly linked-list?
  ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

  Due to way that bounds-checking works, the pointers in the outset may actually point to any
  arbitrary chunk in the chain. However, we must call gib_free_region on the first one to ensure that
  all of them are GC'd. So we need pointers to traverse backward get to the first one.
  'gib_trav_to_first_chunk' accomplishes this.

*/

void gib_insert_into_outset(GibCursor ptr, GibRegionMeta *reg)
{
    uint outset_len = reg->reg_outset_len;
    // Check for duplicates.
    for (uint i = 0; i < outset_len; i++) {
        if (ptr == reg->reg_outset[i]) {
            return;
        }
    }
    // Otherwise, insert into the outset.
    reg->reg_outset[outset_len] = ptr;
    reg->reg_outset_len = outset_len + 1;
    return;
}

void gib_remove_from_outset(GibCursor ptr, GibRegionMeta *reg) {
    uint outset_len = reg->reg_outset_len;
    GibCursor *outset = reg->reg_outset;
    int i;
    if (outset_len == 0) {
        fprintf(stderr, "gib_remove_from_outset: empty outset\n");
        exit(1);
    }
    // Position of 'ptr' in the outset.
    int elt_idx = -1;
    for (i = 0; i < outset_len; i++) {
        if (ptr == outset[i]) {
            elt_idx = i;
        }
    }
    if (elt_idx == -1) {
        fprintf(stderr, "gib_remove_from_outset: element not found\n");
        exit(1);
    }
    // Move all elements ahead of 'elt_idx' back by one position.
    for (i = elt_idx; i < outset_len; i++) {
        outset[i] = outset[i+1];
    }
    return;
}

GibRegionMeta *gib_alloc_region(long long size)
{
    // Allocate the region metadata.
    GibRegionMeta *reg = gib_alloc(sizeof(GibRegionMeta));
    if (reg == NULL) {
        printf("gib_alloc_region: allocation failed: %ld", sizeof(GibRegionMeta));
        exit(1);
    }

    // Allocate the first chunk.
    long long total_size = size + sizeof(GibRegionFooter);
    GibCursor heap;
    bool nursery_allocated = true;
    if (size <= NURSERY_ALLOC_UPPER_BOUND) {
        heap = gib_small_alloc(total_size);
        if (heap == NULL) {
            heap = malloc(total_size);
            nursery_allocated = false;
        }
    } else {
        heap = gib_alloc(total_size);
        nursery_allocated = false;
    }
    if (heap == NULL) {
        printf("gib_alloc_region: malloc failed: %lld", total_size);
        exit(1);
    }
    // Not heap+total_size, since we must keep space for the footer.
    GibCursor heap_end = heap + size;

    // Initialize metadata fields.
    reg->reg_id = gib_gensym();
    reg->reg_refcount = 1;
    reg->reg_heap = heap;
    reg->reg_outset_len = 0;

#ifdef _DEBUG
    printf("Allocated a region(%lld): %lld bytes, nursery=%d.\n", reg->reg_id, size, nursery_allocated);
#endif

    // Write the footer.
    GibRegionFooter *footer = (GibRegionFooter *) heap_end;
    footer->rf_reg_metadata_ptr = reg;
    footer->rf_seq_no = 1;
    footer->rf_nursery_allocated = nursery_allocated;
    footer->rf_size = size;
    footer->rf_next = NULL;
    footer->rf_prev = NULL;

    return reg;
}

inline GibRegionMeta *gib_alloc_counted_region(long long size)
{
    // Bump the count.
    gib_bump_global_region_count();
    return gib_alloc_region(size);
}

GibChunk gib_alloc_chunk(GibCursor end_old_chunk)
{
    // Get size from current footer.
    GibRegionFooter *footer = (GibRegionFooter *) end_old_chunk;
    long long newsize = footer->rf_size * 2;
    // See #110.
    if (newsize > gib_global_max_chunk_size) {
        newsize = gib_global_max_chunk_size;
    }
    long long total_size = newsize + sizeof(GibRegionFooter);

    // Allocate.
    GibCursor start = gib_alloc(total_size);
    if (start == NULL) {
        printf("gib_alloc_chunk: malloc failed: %lld", total_size);
        exit(1);
    }
    GibCursor end = start + newsize;

    // Link the next chunk's footer.
    footer->rf_next = (GibRegionFooter *) end;

    // Write the footer.
    GibRegionFooter* new_footer = (GibRegionFooter *) end;
    new_footer->rf_reg_metadata_ptr = footer->rf_reg_metadata_ptr;
    new_footer->rf_seq_no = footer->rf_seq_no + 1;
    new_footer->rf_nursery_allocated = false;
    new_footer->rf_size = newsize;
    new_footer->rf_next = NULL;
    new_footer->rf_prev = footer;

#ifdef _DEBUG
    GibRegionMeta *reg = (GibRegionMeta*) new_footer->rf_reg_metadata_ptr;
    printf("gib_alloc_chunk: allocated %lld bytes for region %lld.\n", total_size, reg->reg_id);
#endif

    return (GibChunk) {start , end};
}

GibRegionFooter *gib_trav_to_first_chunk(GibRegionFooter *footer)
{
    if (footer->rf_seq_no == 1) {
        return footer;
    } else if (footer->rf_prev == NULL) {
        fprintf(stderr, "No previous chunk found at rf_seq_no: %lld", footer->rf_seq_no);
        return NULL;
    } else {
        gib_trav_to_first_chunk((GibRegionFooter *) footer->rf_prev);
    }
    return NULL;
}

inline uint gib_get_ref_count(GibCursor end_ptr)
{
    GibRegionFooter *footer = (GibRegionFooter *) end_ptr;
    GibRegionMeta *reg = (GibRegionMeta *) footer->rf_reg_metadata_ptr;
    return reg->reg_refcount;
}

// B is the pointer, and A is the pointee (i.e B -> A).
// Bump A's refcount and update B's outset.
inline void gib_bump_refcount(GibCursor end_b, GibCursor end_a)
{
    if (end_a == end_b) {
#ifdef _DEBUG
        printf("gib_bump_refcount: indirection within a region found, refcount not bumped. TODO.\n");
#endif
        return;
    }
    // Grab footers.
    GibRegionFooter *footer_a = (GibRegionFooter *) end_a;
    GibRegionFooter *footer_b = (GibRegionFooter *) end_b;

    // Grab metadata.
    GibRegionMeta *reg_a = (GibRegionMeta *) footer_a->rf_reg_metadata_ptr;
    GibRegionMeta *reg_b = (GibRegionMeta *) footer_b->rf_reg_metadata_ptr;

    // Bump A's refcount.
    uint current_refcount, new_refcount;
    current_refcount = reg_a->reg_refcount;
    new_refcount = current_refcount + 1;
    reg_a->reg_refcount = new_refcount;

#ifdef _DEBUG
    printf("gib_bump_refcount: %lld -> %lld\n", reg_b->reg_id, reg_a->reg_id);
    printf("gib_bump_refcount: old-refcount=%d, old-outset-len=%d:\n", current_refcount, reg_b->reg_outset_len);
    assert(current_refcount == reg_b->reg_outset_len+1);
#endif

    // Add A to B's outset.
    gib_insert_into_outset(end_a, reg_b);

#ifdef _DEBUG
    // printf("gib_bump_refcount: Added %p to %lld's outset, %p.\n", end_a, reg_b->reg_id, reg_b);
    printf("gib_bump_refcount: new-refcount=%d, new-outset-len=%d\n", new_refcount, reg_b->reg_outset_len);
    assert(new_refcount == reg_b->reg_outset_len+1);
#endif

    return;
}

void gib_free_region(GibCursor end_reg) {
    // Grab footer and the metadata.
    GibRegionFooter *footer = (GibRegionFooter *) end_reg;
    GibRegionMeta *reg = (GibRegionMeta *) footer->rf_reg_metadata_ptr;

    //
    GibRegionFooter *first_chunk_footer, *next_chunk_footer;
    GibCursor first_chunk, next_chunk;

    // Decrement current reference count.
    uint current_refcount, new_refcount;
    current_refcount = reg->reg_refcount;
    new_refcount = 0;
    if (current_refcount != 0) {
        new_refcount = current_refcount - 1;
        reg->reg_refcount = new_refcount;
    }

#ifdef _DEBUG
    printf("gib_free_region(%lld): refcounts (1): old-refcount=%d, new-refcount=%d:\n", reg->reg_id, current_refcount, new_refcount);
#endif


    // Free this region recount is 0.
    if (new_refcount == 0) {

#ifdef _DEBUG
        printf("gib_free_region(%lld): outset length: %d\n", reg->reg_id, reg->reg_outset_len);
#endif

        // Decrement refcounts, free regions with refcount==0 and also free
        // elements of the outset.
        if (reg->reg_outset_len != 0) {
            uint outset_len = reg->reg_outset_len;
            GibCursor *outset = reg->reg_outset;
            GibRegionFooter *elt_footer;
            GibRegionMeta *elt_reg;
            uint elt_current_refcount, elt_new_refcount;
            GibCursor to_be_removed[MAX_OUTSET_LENGTH];
            uint to_be_removed_idx = 0;
            for (int i = 0; i < outset_len; i++) {
                elt_footer = (GibRegionFooter *) outset[i];
                elt_reg = (GibRegionMeta *) elt_footer->rf_reg_metadata_ptr;
#ifdef _DEBUG
                elt_current_refcount = elt_reg->reg_refcount;
#endif
                elt_new_refcount = elt_current_refcount - 1;
                elt_reg->reg_refcount = elt_new_refcount;
#ifdef _DEBUG
                printf("gib_free_region(%lld): old-refcount=%d, new-refcount=%d:\n",
                       elt_reg->reg_id, elt_current_refcount, elt_reg->reg_refcount);
#endif
                if (elt_new_refcount == 0) {
                    // See [Why is it a doubly linked-list?] above
                    first_chunk_footer = gib_trav_to_first_chunk(elt_footer);
                    if (first_chunk_footer != NULL) {
                        gib_free_region((GibCursor) first_chunk_footer);
                    }
                }
                to_be_removed[to_be_removed_idx] = outset[i];
                to_be_removed_idx++;
            }
            // Remove elements from the outset.
            for (uint i = 0; i < to_be_removed_idx; i++) {
                gib_remove_from_outset(to_be_removed[i], reg);
            }
        }


#ifdef _DEBUG
        // Bookkeeping
        long long num_freed_chunks = 0, total_bytesize = 0;
#endif

        // Free the chunks in this region.
        first_chunk = end_reg - footer->rf_size;
        first_chunk_footer = footer;
        next_chunk = (char*) footer->rf_next;

#ifdef _DEBUG
        printf("gib_free_region(%lld): first chunk in nursery: %d\n",
               reg->reg_id,
               first_chunk_footer->rf_nursery_allocated);
#endif

        if (! first_chunk_footer->rf_nursery_allocated) {
#ifdef _DEBUG
            num_freed_chunks++;
            total_bytesize = total_bytesize + first_chunk_footer->rf_size;
#endif
            free(first_chunk);
        }

        while (next_chunk != NULL) {
            next_chunk_footer = (GibRegionFooter *) next_chunk;
#ifdef _DEBUG
            num_freed_chunks++;
            total_bytesize = total_bytesize + next_chunk_footer->rf_size;
#endif
            free(next_chunk - next_chunk_footer->rf_size);
            next_chunk = (char*) next_chunk_footer->rf_next;
        }

#ifdef _DEBUG
        printf("gib_free_region(%lld): Freed %lld bytes across %lld chunks.\n",
               reg->reg_id, total_bytesize, num_freed_chunks);
#endif

        // Free the metadata.
        free(reg);

    } else {
#ifdef _DEBUG
        printf("gib_free_region(%lld): non-zero refcount: %d.\n",
               reg->reg_id, reg->reg_refcount);
#endif
    }
}

// Assume that all nodes with size information have tags >= 150.
GibBool gib_is_big(GibInt i, GibCursor cur)
{
    GibPackedTag tag = *(GibPackedTag *) cur;
    if (tag >= 150) {
        cur += 1;
        GibInt size = *(GibInt *) cur;
        if (size >= i) {
            return true;
        } else {
            return false;
        }
    }
    return false;
}

